package residenza;

public class Ospite {
	
	private String nomeO;
	private String cognomeO;
	private String dataO;
	
	public String getNomeO() {
		return nomeO;
	}
	public void setNomeO(String nomeO) {
		this.nomeO = nomeO;
	}
	
	public String getCognomeO() {
		return cognomeO;
	}
	public void setCognomeO(String cognomeO) {
		this.cognomeO = cognomeO;
	}
	
	public String getDataO() {
		return dataO;
	}
	public void setDataO(String dataO) {
		this.dataO = dataO;
	}
}
